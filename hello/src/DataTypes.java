import java.util.Scanner;
public class DataTypes {
	public static void main(String[] args) {
	int a=10;
    System.out.println(a);
    char b='A';
    System.out.println(b);
    float c=4.4f;
    System.out.println(c);
    double d=33.333d;
    System.out.println(d);
    boolean e=false;
    System.out.println(e);
	}
}
